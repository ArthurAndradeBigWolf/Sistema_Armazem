class Origem{
  private String pais;
  private String continente;

  //CONSTRUTOR
  public Origem(){
    pais = "Alemanha";
    continente = "Europa";
  }

  //SETS

  public void setPais(String pais){
    this.pais = pais;
  }

  public void setContinente(String continente){
    this.continente = continente;
  }

  //GETS

  public String getPais(){
    return pais;
  }

  public String getContinente(){
    return continente;
  }


}
